function [e2, e3] = invDynXY(quat)
Rxy = quat2dcm(quat);
% alpha = 0;
beta = pi/12;
gamma = pi/12;
a11 = Rxy(1,1);
% a12 = Rxy(1,2);
a13 = Rxy(1,3);
a21 = Rxy(2,1);
% a22 = Rxy(2,2);
a23 = Rxy(2,3);
a31 = Rxy(3,1);
a32 = Rxy(3,2);
a33 = Rxy(3,3);
sb = sin(beta);
% cb = cos(beta);
sg = sin(gamma);
cg = cos(gamma);
se2 = (a23*cg-a21*sg)/sb;
ce2 = (a13*cg-a11*sg)/sb;
se3 = a32/sb;
ce3 = -(a31*cg+a33*sg)/sb;
e2 = atan2(se2, ce2);
e3 = atan2(se3, ce3);
